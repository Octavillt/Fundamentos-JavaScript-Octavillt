// Declaración de variables

const numero1 = 20; // Se declara una constante llamada 'numero1' y se le asigna el valor 20.
const numero2 = "20"; // Se declara 'numero2' con un valor "20", que es una cadena de texto, no un número.
const numero3 = 30; // Se declara 'numero3' con el valor 30.

// Uso del operador 'mayor que' (>)
// Muestra 'false' en la consola, ya que 20 no es mayor que 30.
console.log(numero1 > numero3); // false
// Muestra 'true', pues 30 es mayor que 20. 
console.log(numero3 > numero1); // true

// Uso del operador 'menor que' (<)
// Muestra 'false', porque 30 no es menor que 20.
console.log(numero3 < numero1); // false
