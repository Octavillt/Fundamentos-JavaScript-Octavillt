let dinero = 639; // Declara una variable 'dinero' con el valor 639.
let totalCarrito = 333; // Declara una variable 'totalCarrito' con el valor 333.

// Utiliza el operador '>' para comparar si 'dinero' es mayor que 'totalCarrito'.
if (dinero > totalCarrito) {
    console.log('Pago Correcto'); // Se ejecuta si 'dinero' es mayor.
} else {
    console.log('Fondos Insuficientes'); // Se ejecuta si 'dinero' no es mayor.
}

