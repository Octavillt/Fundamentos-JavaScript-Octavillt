// Declaración de Números en JavaScript

// Declaración de números como literales
const numero1 = 30; // Declaramos 'numero1' como un número entero.
const numero2 = 20; // Declaramos 'numero2' como otro número entero.
const numero3 = 20.20; // Declaramos 'numero3' como un número con decimales.
const numero4 = .10213; // Declaramos 'numero4', un número con decimales que comienza con un punto.
const numero5 = -3; // Declaramos 'numero5' como un número entero negativo.

// Diferencia entre Números y Strings
const numero6 = 20; // 'numero6' es un número.
const numero7 = "20"; // 'numero7' es un string, no un número, a pesar de contener caracteres numéricos.

// Muestra los valores de 'numero6' y 'numero7'.
console.log(numero6, numero7); // 20 '20'

// Uso del Constructor de Números (menos común)
const numero8 = new Number(20); // 'numero8' es creado usando el constructor de números.

// Muestra el valor de 'numero8'.
console.log(numero8); // [Number: 20]
