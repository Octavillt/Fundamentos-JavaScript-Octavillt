// .toUpperCase() Te va a Permitir Cambiar un Texto Todo a Mayusculas
const producto = 'Monitor 24 pulgadas ';
console.log(producto.toUpperCase());
// .toUpperCase() convierte todos los caracteres de 'producto' a mayúsculas. Resultado: 'MONITOR 24 PULGADAS '.

// .toLowerCase() Te va a Permitir Cambiar un Texto Todo a Minusculas
console.log(producto.toLowerCase());
// .toLowerCase() convierte todos los caracteres de 'producto' a minúsculas. Resultado: 'monitor 24 pulgadas '.

// .toString() Se Utiliza para Cambiar un Numero a String
const cantidad = 200;
console.log(cantidad);
// Muestra el valor de 'cantidad' como número.

console.log(cantidad.toString());
// .toString() convierte el número 'cantidad' en una cadena de texto. Resultado: '200' como string.
