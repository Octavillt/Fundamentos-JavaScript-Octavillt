// Uso del operador ternario con valores booleanos

const boolean1 = true; // Se declara 'boolean1' como un booleano con el valor 'true'.
const boolean2 = false; // Se declara 'boolean2' como un booleano con el valor 'false'.

// Uso de un condicional 'if' estándar
if (boolean1) {
    console.log('si es true'); // Se ejecuta si 'boolean1' es 'true'.
} else {
    console.log('no, no es true'); // Se ejecuta si 'boolean1' no es 'true'.
}

// Simplificación con el operador ternario

// El operador ternario es una forma más concisa de escribir una instrucción 'if-else'.
console.log(boolean1 ? 'Si es true' : 'No no es true');
// Si 'boolean1' es 'true', se ejecuta la parte antes de ':'. Si es 'false', se ejecuta la parte después de ':'.
