/*
    Funciones del Lenguaje JavaScript
    JavaScript, al igual que otros lenguajes de programación como Java, Python y PHP, 
    incluye varias funciones incorporadas que son fundamentales para la programación en este lenguaje.
*/

/*
    'window' en el contexto del navegador
    En un entorno de navegador, 'window' es un objeto global que representa la ventana del navegador.
    Muchas funciones y variables están disponibles en el objeto 'window'.
*/

// Ejemplo de función 'alert'
alert('Hubo un error...');
// 'alert' es una función de navegador que muestra una ventana de diálogo con un mensaje.
// Es útil para informar a los usuarios sobre eventos específicos o errores.

// Ejemplo de función 'prompt'
prompt('Cual es tu edad?');
// 'prompt' es otra función de navegador que muestra una ventana de diálogo con un campo de texto para que el usuario ingrese información.
// Se utiliza para obtener datos del usuario, como en este caso, su edad.

// Ejemplo de uso de 'console.log' y 'parseInt'
console.log(parseInt('3'));
/*
    'console.log' es una función utilizada para imprimir información en la consola del navegador o de Node.js.
    Es una herramienta esencial para depurar código.
    'parseInt' es una función que convierte una cadena de texto en un número entero.
    Aquí, se convierte la cadena '3' en el número 3 y luego se imprime usando 'console.log'.
*/
/*
    Estos son ejemplos de algunas de las funciones incorporadas en JavaScript.
    Puedes ver que estas funciones tienen un nombre y se llaman utilizando paréntesis.
    Estas funciones son parte del "core" de JavaScript y están disponibles en la mayoría de los
    entornos de ejecución de JavaScript.
*/