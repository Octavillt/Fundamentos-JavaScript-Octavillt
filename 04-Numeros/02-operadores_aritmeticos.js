// Operaciones

// Declaración de números
const numero1 = 30; // Declaramos 'numero1' como un número entero.
const numero2 = 20; // Declaramos 'numero2' como otro número entero.

let resultado; // Declaramos una variable 'resultado' para almacenar los resultados de las operaciones.

// Suma
resultado = numero1 + numero2; // Sumamos 'numero1' y 'numero2'. Resultado: 50.

// Resta
resultado = numero1 - numero2; // Restamos 'numero2' de 'numero1'. Resultado: 10.

// Multiplicación
resultado = numero1 * numero2; // Multiplicamos 'numero1' por 'numero2'. Resultado: 600.

// División
resultado = numero1 / numero2; // Dividimos 'numero1' entre 'numero2'. Resultado: 1.5.

// Módulo
resultado = numero1 % numero2; // Calculamos el módulo (resto de la división) de 'numero1' por 'numero2'. Resultado: 10.

console.log(resultado); // Muestra el último valor calculado en 'resultado', que es 10.
